package am;

public class IfTest7 {

	public static void main(String[] args) {
		// 프로그램 시작
		
		// if~else문의 구성
		/*
		 *  if(조건식)
		 *  	조건식에 만족할 경우 수행할 문장;
		 *  else
		 *  	조건식에 불만족 시 수행할 문장;
		 */
		int su1 = 5;
		
		if (su1 > 10) {
			System.out.println("10초과");
		} else {
			System.out.println("10이하");
		}
		
		// 60점 이상 합격 그 미만 불합격
	}

}
